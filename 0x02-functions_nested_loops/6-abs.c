/**
 * _abs - takes int n
 * Description: a function that computes the absolute value of an integer
 * @n: parameter format to work on
 * Return: combuted absolute value
 */

int _abs(int n)
{
	if (n < 0)
	{
		return ((-1) * n);
	}
	else if (n > 0)
	{
		return (n);
	}
	else
	{
		return (n);
	}
}


